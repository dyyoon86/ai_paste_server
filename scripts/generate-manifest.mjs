#!/usr/bin/env node
/**
 * Scans src/content/remotion-skills and writes src/generated/remotionSkillsManifest.ts.
 *
 * The parsing logic here mirrors src/lib/skillsScanner.ts (which is the unit-tested
 * canonical implementation). This script is intentionally dependency-free so it can
 * run as a pre-dev / pre-build hook on any machine.
 */
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const CONTENT_DIR = path.join(ROOT, "src", "content", "remotion-skills");
const OUT_FILE = path.join(ROOT, "src", "generated", "remotionSkillsManifest.ts");

function parseFrontmatter(rawMarkdown) {
  const markdown = rawMarkdown.replace(/\r\n/g, "\n");
  const fmMatch = markdown.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  if (!fmMatch) return { tags: [], body: markdown };
  const [, fmBlock, body] = fmMatch;
  const fm = { tags: [], body };
  let inMetadata = false;
  for (const line of fmBlock.split("\n")) {
    const nameMatch = line.match(/^name:\s*(.+)$/);
    if (nameMatch) { fm.name = nameMatch[1].trim(); continue; }
    const descMatch = line.match(/^description:\s*(.+)$/);
    if (descMatch) { fm.description = descMatch[1].trim(); continue; }
    if (/^metadata:\s*$/.test(line)) { inMetadata = true; continue; }
    const tagsMatch = line.match(/^\s*tags:\s*(.+)$/);
    if (tagsMatch) {
      fm.tags = tagsMatch[1].split(",").map((t) => t.trim()).filter(Boolean);
      continue;
    }
    if (inMetadata && /^\S/.test(line)) inMetadata = false;
  }
  return fm;
}

function categorize(id, tags) {
  const has = (s) => id.includes(s);
  if (id === "skill" || id === "compositions" || id === "parameters" || id === "calculate-metadata") return "core";
  if (has("text") || tags.includes("typography") || tags.includes("text")) return "text";
  if (has("transition") || has("trimming")) return "transition";
  if (id === "timing") return "timing";
  if (has("sequenc")) return "sequence";
  if (has("font")) return "font";
  if (has("image") || has("gif") || has("lottie") || has("maplibre")) return "image";
  if (has("video") || has("transparent") || has("3d")) return "video";
  if (has("audio") || has("voiceover") || has("sfx") || has("silence") || has("get-audio")) return "audio";
  if (has("caption") || has("subtitle") || has("srt") || has("transcribe")) return "caption";
  if (has("light-leak") || has("html-in-canvas") || has("audio-visualization")) return "effect";
  if (has("measuring-dom") || has("tailwind")) return "layout";
  return "unknown";
}

function titleCase(id) {
  return id.split(/[-_]/).map((w) => (w.length ? w[0].toUpperCase() + w.slice(1) : w)).join(" ");
}

function deriveSummary(fm) {
  if (fm.description) return fm.description;
  for (const raw of fm.body.split("\n")) {
    const line = raw.trim();
    if (line && !line.startsWith("#")) return line.slice(0, 160);
  }
  return "Remotion best-practice rule.";
}

async function collect() {
  const docs = [];
  const skillPath = path.join(CONTENT_DIR, "SKILL.md");
  try {
    const md = await fs.readFile(skillPath, "utf8");
    docs.push(toDoc("skill", "src/content/remotion-skills/SKILL.md", md));
  } catch {
    // SKILL.md missing — keep going.
  }

  const rulesDir = path.join(CONTENT_DIR, "rules");
  let entries = [];
  try {
    entries = await fs.readdir(rulesDir, { withFileTypes: true });
  } catch {
    entries = [];
  }
  for (const e of entries) {
    if (!e.isFile() || !e.name.endsWith(".md")) continue;
    const id = e.name.replace(/\.md$/, "");
    const md = await fs.readFile(path.join(rulesDir, e.name), "utf8");
    docs.push(toDoc(id, `src/content/remotion-skills/rules/${e.name}`, md));
  }
  docs.sort((a, b) => a.id.localeCompare(b.id));
  return docs;
}

function toDoc(id, relPath, markdown) {
  const fm = parseFrontmatter(markdown);
  return {
    id,
    title: titleCase(fm.name ?? id),
    path: relPath,
    category: categorize(id, fm.tags),
    tags: fm.tags,
    summary: deriveSummary(fm),
    rawMarkdown: markdown,
  };
}

async function main() {
  const docs = await collect();
  await fs.mkdir(path.dirname(OUT_FILE), { recursive: true });

  const header = `// AUTO-GENERATED by scripts/generate-manifest.mjs — do not edit by hand.
// Source: src/content/remotion-skills/** (installed via \`npx skills add remotion-dev/skills\`)
import type { RemotionSkillDoc } from "@/lib/skillsScanner";

export const remotionSkillDocs: RemotionSkillDoc[] = ${JSON.stringify(docs, null, 2)};

export const remotionSkillDocsById: Record<string, RemotionSkillDoc> = Object.fromEntries(
  remotionSkillDocs.map((d) => [d.id, d]),
);
`;
  await fs.writeFile(OUT_FILE, header, "utf8");
  console.log(`[manifest] wrote ${docs.length} skill docs -> ${path.relative(ROOT, OUT_FILE)}`);
}

main().catch((err) => {
  console.error("[manifest] failed:", err);
  process.exit(1);
});
